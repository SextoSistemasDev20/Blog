
#include <iostream>
/*
En esta ocasion mostrare en c++ la manera de confirmar
si x numero es palindromo o no.

un numero palindromo es: describi


por lo que para calcular si lo es o no abria que

*/
using namespace std;

main()
{
    int numero,p,s,t,c,m;


cout<<"ingrese un numero ";
cin>>numero;
p=numero%10;
s=p/10;
c=s/10;
m=p*100+t*10+c;
if (m=numero)
{
cout<<"El Numero es Palindromo\n";
}
else
{
cout<<"El Numero no es Palindromo\n";
}

}

